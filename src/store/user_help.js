export default class User {
    constructor(
        surname,
        firstname,
        username,
        email,
        realm,
        password,
        date,
        time,
        id,
    ) {
            this.surname = surname,
            this.firstname = firstname,
            this.username = username,
            this.email = email,
            this.realm = realm,
            this.password = password,
            this.date = date,
            this.time = time,
            this.id = id
    }
}